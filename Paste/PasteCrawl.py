def random_headers():
    return {'User-Agent': choice(desktop_agents),
            'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,*/*;q=0.8'}


def is_pasebin_url(url):
    url = list(filter(None, url.replace(" ", "").splitlines()))[0]
    if re.search("(\W?)pastebin(\W|$)", url):
        return "https://pastebin.com/raw/" + url.split("/")[-1]
    else:
        return False


def get_pastebin_content(url):
    if url:
        s = requests.Session()
        r = s.get(url, headers=random_headers())
        if r.status_code == 200:
            return r.content
        else:
            return r.status_code
    else:
        return False
